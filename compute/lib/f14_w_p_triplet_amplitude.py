@dataclass(frozen=True)
class TripletShadowUpperBound:
    """Triangle-inequality upper bound on |S_r(W(p))|.

    bound_TT_exact : exact |S_r^TT(W(p))|
    bound_TW_plus_WW : Adamovic-Milas bound on |S_r^TW| + |S_r^WW|
    bound_total : sum (triangle upper bound)
    bound_total_over_factorial : |S_r| / r!  envelope value
    rate : (|S_r| / r!)^{1/r}  Stirling rate
    """

    p: int
    r: int
    bound_TT_exact: Fraction
    bound_TW_plus_WW: Fraction
    bound_total: Fraction
    bound_total_over_factorial: Fraction
    rate: float


def triplet_shadow_upper_bound(
    p: int, r: int, *, virasoro_seq: Mapping[int, Fraction]
) -> TripletShadowUpperBound:
    """Return the triangle upper bound on |S_r(W(p))|.

    Parameters
    ----------
    p : int
    r : int
    virasoro_seq : Mapping[int, Fraction]
        Output of virasoro_subchannel_at_cp(p, max_r >= r) used as the
        exact TT subchannel.

    Returns
    -------
    TripletShadowUpperBound
        |S_r^TT(W(p))| + Adamovic-Milas TW+WW bound, plus Stirling rate.
    """
    if r < 1:
        raise ValueError(f"shadow arity must be positive, got {r}")
    if r not in virasoro_seq:
        raise KeyError(f"virasoro_seq missing r={r}")
    bound = adamovic_milas_amplitude_bound(p)
    s_tt = abs(virasoro_seq[r])
    s_other = adamovic_milas_raw_bound(bound, r)
    total = s_tt + s_other
    r_factorial = math.factorial(r)
    rate = (
        0.0
        if total == 0
        else float(total) ** (1.0 / r) / r_factorial ** (1.0 / r)
    )
    return TripletShadowUpperBound(
        p=p,
        r=r,
        bound_TT_exact=s_tt,
        bound_TW_plus_WW=s_other,
        bound_total=total,
        bound_total_over_factorial=Fraction(total) / Fraction(r_factorial),
        rate=rate,
    )


def w3_shadow_upper_bound_table(max_r: int = 8) -> dict[int, TripletShadowUpperBound]:
    """Return the |S_r(W(3))| upper bound for r = 1, ..., max_r.

    Parameters
    ----------
    max_r : int
        Highest arity to record. Default 8 per the F14 mission.

    Returns
    -------
    dict[int, TripletShadowUpperBound]
        Mapping r -> upper bound data.
    """
    if max_r < 1:
        raise ValueError(f"max_r must be positive, got {max_r}")
    virasoro = virasoro_subchannel_at_cp(3, max_r=max(max_r, 4))
    out = {}
    for r in range(1, max_r + 1):
        out[r] = triplet_shadow_upper_bound(3, r, virasoro_seq=virasoro)
    return out


