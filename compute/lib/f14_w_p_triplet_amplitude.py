# ===========================================================================
# Tempering certificate via the Adamovic-Milas bound
# ===========================================================================


def adamovic_milas_log_tempered_rate(
    bound: AdamovicMilasAmplitudeBound, r: int, *, include_catalan: bool = True
) -> float:
    """Return log( (raw bound) / r! )^{1/r}.

    Parameters
    ----------
    bound : AdamovicMilasAmplitudeBound
    r : int
    include_catalan : bool
        If True (default), include the Cat_{r-1} factor.  If False, use
        only the polynomial-exponential factor C_p r^{N_p} R_p^r.

    Returns
    -------
    float
        log of the Stirling rate.
    """
    if r < 2:
        raise ValueError(f"tempered rate requires r >= 2, got {r}")
    log_bound = (
        math.log(bound.C_p)
        + bound.N_p * math.log(r)
        + r * math.log(float(bound.R_p))
    )
    if include_catalan:
        # log Cat_{r-1} ~ (r-1) log 4 - (3/2) log(r-1) - log(sqrt pi)
        cat = _catalan(r - 1)
        log_bound += math.log(cat)
    return (log_bound - lgamma(r + 1)) / r


def adamovic_milas_stirling_limit(bound: AdamovicMilasAmplitudeBound) -> float:
    """Return lim_{r->infty} (bound / r!)^{1/r} = 0 by Stirling.

    Numerical certificate via large-r evaluation.
    """
    return adamovic_milas_log_tempered_rate(bound, r=10_000)


