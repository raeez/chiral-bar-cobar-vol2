@dataclass(frozen=True)
class AdamovicMilasAmplitudeBound:
    """Adamovic-Milas amplitude bound constants (C_p, N_p, R_p) for W(p).

    The bound takes the form
        |S_r^TW(W(p))| + |S_r^WW(W(p))| <= C_p * r^{N_p} * R_p^r * Cat_{r-1}.

    Attributes
    ----------
    p : int
        Triplet parameter, p >= 2.
    c_p : Fraction
        Central charge c(p) = 1 - 6(p-1)^2 / p.
    C_p : int
        Adamovic-Milas Zhu dimension dim A(W(p)) = 2p.  Bounds the
        residue-state count in the Felder cohomology.
    N_p : int
        Hilbert-series polynomial degree p - 1.  Forced by the FGST
        theta-character: chi_{W(p)} contains (p-1) theta derivatives.
    R_p : Fraction
        WW bar-pole / |c(p)| ratio = (4p - 3) / |c(p)|.  This is the
        exponential growth base in the Catalan-Cauchy form.
    """

    p: int
    c_p: Fraction
    C_p: int
    N_p: int
    R_p: Fraction

    @property
    def R_p_catalan_tightened(self) -> Fraction:
        """Return 4 * R_p, the exponential base after Catalan tightening.

        Cat_{r-1} <= 4^{r-1}, so the raw bound C_p r^{N_p} R_p^r Cat_{r-1}
        is bounded above by C_p r^{N_p} (4 R_p)^r.
        """
        return 4 * self.R_p


def adamovic_milas_amplitude_bound(p: int) -> AdamovicMilasAmplitudeBound:
    """Return the Adamovic-Milas amplitude-bound constants for W(p).

    Parameters
    ----------
    p : int
        Triplet parameter, must be >= 2.

    Returns
    -------
    AdamovicMilasAmplitudeBound
        Bound constants.

    Notes
    -----
    C_p = 2p (Zhu dimension).
    N_p = p - 1 (FGST theta-character polynomial degree).
    R_p = (4p - 3) / |c(p)| (WW bar pole / Zamolodchikov norm).
    """
    if p < 2:
        raise ValueError(f"triplet parameter p must be >= 2, got {p}")
    c = triplet_central_charge(p)
    abs_c = abs(c)
    # R_p = (4p - 3) / |c(p)|.  abs_c is a Fraction; build the ratio
    # as Fraction((4p-3) * denom, numer).
    R_p = Fraction(ww_bar_pole_order(p), 1) / abs_c
    return AdamovicMilasAmplitudeBound(
        p=p,
        c_p=c,
        C_p=zhu_dimension(p),
        N_p=p - 1,
        R_p=R_p,
    )


def _catalan(n: int) -> int:
    """Return the nth Catalan number."""
    if n < 0:
        raise ValueError(f"Catalan index must be non-negative, got {n}")
    return comb(2 * n, n) // (n + 1)


# ===========================================================================
# Adamovic-Milas amplitude bound: explicit raw form (with Catalan factor)
# ===========================================================================


def adamovic_milas_raw_bound(bound: AdamovicMilasAmplitudeBound, r: int) -> Fraction:
    """Return the raw Adamovic-Milas bound C_p * r^{N_p} * R_p^r * Cat_{r-1}.

    The raw form bounds the actual regular-sector amplitude before Catalan
    tightening.  By convention r = 1 returns 0 (no bar-graph collisions);
    r = 0 returns 0.

    Parameters
    ----------
    bound : AdamovicMilasAmplitudeBound
        Bound constants from adamovic_milas_amplitude_bound(p).
    r : int
        Shadow arity, r >= 0.

    Returns
    -------
    Fraction
        Exact bound value.
    """
    if r < 0:
        raise ValueError(f"shadow arity must be non-negative, got {r}")
    if r < 2:
        return Fraction(0)
    return (
        Fraction(bound.C_p)
        * Fraction(r ** bound.N_p)
        * (bound.R_p ** r)
        * Fraction(_catalan(r - 1))
    )


def adamovic_milas_tightened_bound(
    bound: AdamovicMilasAmplitudeBound, r: int
) -> Fraction:
    """Return the Catalan-tightened Adamovic-Milas bound.

    The tightened form bounds the raw form via Cat_{r-1} <= 4^{r-1}:
        C_p * r^{N_p} * (4 R_p)^r * (1/4).

    Parameters
    ----------
    bound : AdamovicMilasAmplitudeBound
    r : int

    Returns
    -------
    Fraction
        Exact bound.
    """
    if r < 0:
        raise ValueError(f"shadow arity must be non-negative, got {r}")
    if r < 2:
        return Fraction(0)
    return (
        Fraction(bound.C_p)
        * Fraction(r ** bound.N_p)
        * (bound.R_p_catalan_tightened ** r)
        * Fraction(1, 4)
    )


