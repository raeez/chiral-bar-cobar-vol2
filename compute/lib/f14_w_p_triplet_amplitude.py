_VOL1_ROOT = os.path.expanduser("~/chiral-bar-cobar")


def _load_vol1_engine():
    """Import the Vol I shadow_tower_higher_vir module."""
    spec = importlib.util.spec_from_file_location(
        "_f14_vol1_shadow_tower_higher_vir",
        Path(_VOL1_ROOT) / "compute" / "lib" / "shadow_tower_higher_vir.py",
    )
    if spec is None or spec.loader is None:
        raise ImportError(
            f"Cannot locate Vol I shadow_tower_higher_vir module at {_VOL1_ROOT}"
        )
    mod = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(mod)
    return mod


def virasoro_subchannel_at_cp(p: int, max_r: int = 8) -> dict[int, Fraction]:
    """Return exact S_r^TT(W(p)) = S_r(Vir_{c(p)}) for r = 1, ..., max_r.

    Convention: S_1 := 0 (no arity-1 shadow coefficient in the master
    equation), S_2 = c/2, S_3 = 2, and S_4, ..., S_{max_r} from the Vol I
    chain-A recurrence specialised at c = c(p).

    Parameters
    ----------
    p : int
        Triplet parameter.
    max_r : int
        Highest arity to compute.

    Returns
    -------
    dict[int, Fraction]
        S_r^TT(W(p)) as exact Fraction values for r = 1, ..., max_r.
    """
    if p < 2:
        raise ValueError(f"triplet parameter p must be >= 2, got {p}")
    if max_r < 4:
        raise ValueError(f"max_r must be >= 4, got {max_r}")
    import sympy as sp  # local import to avoid global sympy dependency

    engine = _load_vol1_engine()
    c = triplet_central_charge(p)
    c_sympy = sp.Rational(c.numerator, c.denominator)
    seq = engine.virasoro_shadow_sequence(c_sympy, max_r=max_r)
    out: dict[int, Fraction] = {1: Fraction(0)}
    for r, val in seq.items():
        if r > max_r:
            continue
        val = sp.nsimplify(val, rational=True)
        if isinstance(val, sp.Rational):
            out[int(r)] = Fraction(int(val.p), int(val.q))
        else:
            # Generic-c rational reduction at the W(p) central charge.
            simplified = sp.together(val)
            if simplified.is_Rational:
                out[int(r)] = Fraction(int(simplified.p), int(simplified.q))
            else:
                raise ValueError(
                    f"W({p}) c={c} S_{r} did not reduce to Q-rational: {val}"
                )
    return out


