"""W_3 Zamolodchikov PVA: the simplest non-linear W-algebra.

Generators: T (conformal weight 2), W (conformal weight 3), central charge c.
The non-linearity appears in {W_lam W} which involves the composite field
Lambda = :TT: - (3/10)d^2 T (quasi-primary of weight 4).

The OPE (Bouwknegt-Schoutens, hep-th/9210010):

  W(z)W(w) = (c/3)/(z-w)^6 + 2T(w)/(z-w)^4 + dT(w)/(z-w)^3
             + [beta_Lambda Lambda(w) + (3/10)d^2 T(w)]/(z-w)^2
             + [beta_partial_Lambda dLambda(w) + (1/15)d^3 T(w)]/(z-w)

where beta_Lambda = 32/(22+5c),
beta_partial_Lambda = 16/(22+5c), and
Lambda = :TT: - (3/10)d^2 T.

The n-products (a_{(n)} b = Res_{z->w} (z-w)^n a(z)b(w)):

  W_{(5)}W = c/3, W_{(3)}W = 2T, W_{(2)}W = dT,
  W_{(1)}W = (3/10)d^2 T + beta_Lambda Lambda,
  W_{(0)}W = (1/15)d^3 T + beta_partial_Lambda dLambda

The lambda-bracket ({a_lam b} = sum_{n>=0} (a_{(n)} b) lam^n / n!):

  {W_lam W} = (c/360)lam^5 + (T/3)lam^3 + (dT/2)lam^2
              + [beta_Lambda Lambda + (3/10)d^2 T]lam
              + beta_partial_Lambda dLambda + (1/15)d^3 T

Cross-reference: Vol I w3_bar.py (comp:w3-nthproducts).

References:
  - Zamolodchikov (1985): Infinite additional symmetries in 2D CFT
  - Bouwknegt-Schoutens, hep-th/9210010: W-algebra review
  - De Sole-Kac (2006): PVA formalism for W-algebras
"""
from sympy import (Symbol, Rational, symbols, expand, simplify, S, factorial,
                   collect, Poly, degree)
from typing import Dict, Tuple, Optional


# ---------------------------------------------------------------------------
# Generators and translation operator
# ---------------------------------------------------------------------------
# Polynomial PVA generated by T (weight 2) and W (weight 3).
# Derivatives are distinct symbols: T, dT, d2T, d3T, W, dW, d2W, ...
# The composite Lambda = :TT: - (3/10)d^2T is a separate symbol.

T = Symbol('T')
dT = Symbol('dT')
d2T = Symbol('d2T')
d3T = Symbol('d3T')
d4T = Symbol('d4T')

W = Symbol('W')
dW = Symbol('dW')
d2W = Symbol('d2W')

# Composite field: Lambda = :TT: - (3/10)d^2T
# At the PVA level this is a SINGLE symbol representing the quasi-primary
# normal-ordered product.
Lambda = Symbol('Lambda')
dLambda = Symbol('dLambda')
d2Lambda = Symbol('d2Lambda')

# Central charge
c = Symbol('c')

# Standard W_3 nonlinear coefficients.  The Lambda coefficient is
# 32/(22+5c); the derivative coefficient is half of it.
# SINGULAR at c = -22/5.
beta_Lambda = Rational(32, 1) / (22 + 5 * c)
beta_partial_Lambda = Rational(16, 1) / (22 + 5 * c)

# Backward-compatible name used by older tests and scripts.  It now denotes
# the undifferentiated Lambda coefficient.
beta_sq = beta_Lambda


# ---------------------------------------------------------------------------
# Translation operator
# ---------------------------------------------------------------------------

_DERIV_MAP = {
    T: dT, dT: d2T, d2T: d3T, d3T: d4T,
    W: dW, dW: d2W,
    Lambda: dLambda, dLambda: d2Lambda,
}

_ALL_FIELDS = [T, dT, d2T, d3T, d4T, W, dW, d2W,
               Lambda, dLambda, d2Lambda]


def apply_partial(expr):
    """Apply the translation operator d to an expression.

    d acts as a derivation:
      d(T) = dT, d(dT) = d2T, d(d2T) = d3T, d(d3T) = d4T
      d(W) = dW, d(dW) = d2W
      d(Lambda) = dLambda, d(dLambda) = d2Lambda
      d(const) = 0  (including spectral parameters lam, mu, c)
      d(f*g) = d(f)*g + f*d(g)  (Leibniz rule)
    """
    result = S.Zero
    for field in _ALL_FIELDS:
        coeff = expr.diff(field)
        if coeff != 0 and field in _DERIV_MAP:
            result += coeff * _DERIV_MAP[field]
    return expand(result)


# ---------------------------------------------------------------------------
# n-th products
# ---------------------------------------------------------------------------

def w3_nth_products():
    """All singular n-th products for W_3 generators.

    Returns dict of {(a, b): {n: expression}} where expression is
    a differential polynomial in the field symbols.

    Ground truth: Bouwknegt-Schoutens (1993), Vol I comp:w3-nthproducts.
    """
    alpha = beta_sq  # undifferentiated Lambda coefficient 32/(22+5c)
    return {
        # T x T (quartic pole, Virasoro subalgebra)
        ('T', 'T'): {
            3: c / 2,
            1: 2 * T,
            0: dT,
        },
        # T x W (double pole, W is primary of weight 3)
        ('T', 'W'): {
            1: 3 * W,
            0: dW,
        },
        # W x T (asymmetric with T x W via skew-symmetry)
        ('W', 'T'): {
            1: 3 * W,
            0: 2 * dW,
        },
        # W x W (sixth-order pole, the non-linear bracket)
        ('W', 'W'): {
            5: c / 3,
            3: 2 * T,
            2: dT,
            1: Rational(3, 10) * d2T + alpha * Lambda,
            0: Rational(1, 15) * d3T + beta_partial_Lambda * dLambda,
        },
    }


def w3_nth_product(a, b, n):
    """Get a_{(n)} b for generators a, b in {T, W}."""
    products = w3_nth_products()
    pair = (a, b)
    if pair not in products:
        return S.Zero
    return products[pair].get(n, S.Zero)


# ---------------------------------------------------------------------------
# Lambda-brackets
# ---------------------------------------------------------------------------

def _bracket_TT(lam):
    """{T_lam T} = dT + 2*T*lam + (c/12)*lam^3.

    Virasoro subalgebra.
    """
    return dT + 2 * T * lam + (c / 12) * lam**3


def _bracket_TW(lam):
    """{T_lam W} = dW + 3*W*lam.

    W is primary of conformal weight 3 under T.
    """
    return dW + 3 * W * lam


def _bracket_WT(lam):
    """{W_lam T} computed from skew-symmetry: {W_lam T} = -{T_{-lam-d} W}.

    From n-products: W_{(1)}T = 3W, W_{(0)}T = 2dW.
    Lambda-bracket: 2dW + 3W*lam.
    """
    return 2 * dW + 3 * W * lam


def _bracket_WW(lam):
    """{W_lam W}: the non-linear bracket.

    From n-products via {a_lam b} = sum_{n>=0} (a_{(n)} b) lam^n / n!:

    {W_lam W} = (c/360)lam^5 + (T/3)lam^3 + (dT/2)lam^2
                + [beta_Lambda Lambda + (3/10)d^2T]lam
                + beta_partial_Lambda dLambda + (1/15)d^3T

    The factorials: 5!/5!=1 (n=5 -> lam^5/120 * c/3 = c/360*lam^5),
    3!/3!=1 (n=3 -> lam^3/6 * 2T = T/3*lam^3),
    2!/2!=1 (n=2 -> lam^2/2 * dT = dT/2*lam^2),
    1!/1!=1 (n=1), 0!/0!=1 (n=0).
    """
    alpha = beta_sq
    return (Rational(1, 360) * c * lam**5
            + Rational(1, 3) * T * lam**3
            + Rational(1, 2) * dT * lam**2
            + (alpha * Lambda + Rational(3, 10) * d2T) * lam
            + beta_partial_Lambda * dLambda + Rational(1, 15) * d3T)


def w3_lambda_bracket(a, b, lam):
    """Compute {a_lam b} for the W_3 PVA.

    Parameters:
        a, b: strings 'T' or 'W'
        lam: spectral parameter symbol

    Returns:
        Symbolic expression (polynomial in lam with field coefficients).
    """
    key = (a, b)
    if key == ('T', 'T'):
        return _bracket_TT(lam)
    elif key == ('T', 'W'):
        return _bracket_TW(lam)
    elif key == ('W', 'T'):
        return _bracket_WT(lam)
    elif key == ('W', 'W'):
        return _bracket_WW(lam)
    else:
        raise ValueError(f"Unknown generator pair: ({a}, {b})")


