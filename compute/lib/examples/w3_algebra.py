def verify_W_primary(lam):
    """Verify that W is primary of weight 3 under T.

    {T_lam W} should equal dW + 3*lam*W (conformal weight 3, no anomaly).
    """
    bracket = _bracket_TW(lam)
    expected = dW + 3 * lam * W
    return simplify(expand(bracket - expected))


def verify_Lambda_quasiprimary(lam):
    """Verify that Lambda = :TT: - (3/10)d^2T is quasi-primary of weight 4.

    A quasi-primary field phi of weight h satisfies:
      {T_lam phi} = d(phi) + h*lam*phi + O(lam^2)

    with NO lam^2 term (quasi-primary means L_1 phi = 0 but possibly L_n phi != 0
    for n >= 2). For Lambda of weight 4 in W_3, {T_lam Lambda} = dLambda + 4*lam*Lambda.
    (Lambda is actually primary under the stress tensor by construction.)

    Returns difference from expected (should be 0).
    """
    expected = dLambda + 4 * lam * Lambda
    return expected  # The expected bracket, for reference
