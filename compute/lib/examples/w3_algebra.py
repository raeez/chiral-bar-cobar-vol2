def w3_ope_coefficients():
    """Return the W_3 OPE as {(a, b): {pole_order: coefficient}}.

    OPE convention: a(z)b(w) ~ sum_n (a_{(n)} b)(w) / (z-w)^{n+1}.

    Pole order = n+1 where n is the n-product index.
    """
    products = w3_nth_products()
    ope = {}
    for (a, b), nprods in products.items():
        ope[(a, b)] = {}
        for n, coeff in nprods.items():
            ope[(a, b)][n + 1] = coeff
    return ope


