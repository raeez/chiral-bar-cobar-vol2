class RestrictedQGStructure:
    """Detailed PBW structure of ubar_q(sl_2) for comparison with bar complex.

    The restricted quantum group has a filtration by powers of E and F:
      F^0: ubar_q = sum_{a=0}^{N-1} sum_{b=0}^{N-1} sum_{c=0}^{N-1} C * E^a F^b K^c

    The grading by (E-degree, F-degree, K-degree) gives a PBW decomposition.

    To compare with the bar complex of L_k, we need to match:
    - E with the Koszul dual of f (lowering operator becomes raising in dual)
    - F with the Koszul dual of e
    - K with the Koszul dual of h (Cartan element)

    The grading match:
    - sl_2 weight of E^a F^b K^c = 2a - 2b (same as e/f generators)
    - The "bar degree" in the QG corresponds to a+b+c (total monomial degree)
    - The quantum Serre relation E^N = F^N = 0 is the dual of f^{N} = 0
      (the null vector relation)
    """

    def __init__(self, k: int):
        self.k = k
        self.N = k + 2
        self.q = np.exp(1j * np.pi / (k + 2))

    def pbw_decomposition(self) -> Dict[Tuple[int, int], int]:
        """Decompose ubar_q by (total degree, sl_2-weight).

        Basis: {E^a F^b K^c : 0 <= a,b,c < N}
        - total degree d = a + b (NOT counting K, which is group-like)
        - sl_2 weight = 2(a - b)

        Return: dict (d, sl2_wt) -> multiplicity
        """
        N = self.N
        decomp: Dict[Tuple[int, int], int] = {}

        for a in range(N):
            for b in range(N):
                for c in range(N):
                    d = a + b  # total E/F degree
                    sl2_wt = 2 * (a - b)
                    key = (d, sl2_wt)
                    decomp[key] = decomp.get(key, 0) + 1

        return decomp

    def dimension_by_degree(self) -> Dict[int, int]:
        """Dimension of ubar_q at each total degree d = a+b.

        For each d, sum over all (a, b, c) with a+b = d, 0 <= a,b,c < N.
        """
        N = self.N
        result: Dict[int, int] = {}
        for d in range(2 * N - 1):  # d ranges from 0 to 2(N-1)
            count = 0
            for a in range(min(d + 1, N)):
                b = d - a
                if 0 <= b < N:
                    count += N  # N choices for c (K^0, ..., K^{N-1})
            result[d] = count
        return result

    def dimension_by_sl2_weight(self) -> Dict[int, int]:
        """Dimension of ubar_q at each sl_2 weight."""
        N = self.N
        result: Dict[int, int] = {}
        for a in range(N):
            for b in range(N):
                sl2_wt = 2 * (a - b)
                result[sl2_wt] = result.get(sl2_wt, 0) + N
        return result

    def verify_dimension(self) -> bool:
        """Verify total dimension is N^3."""
        return sum(self.dimension_by_degree().values()) == self.N ** 3


