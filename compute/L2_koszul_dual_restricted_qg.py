class RestrictedQuantumGroup:
    """The restricted (small) quantum group ubar_q(sl_2).

    At q = e^{i*pi/(k+2)}, the restricted quantum group is the
    finite-dimensional Hopf algebra generated by E, F, K with:
      K^N = 1, E^N = F^N = 0
      KE = q^2 EK, KF = q^{-2} FK
      EF - FE = (K - K^{-1})/(q - q^{-1})

    where N = k+2 (the order of q^2 as a root of unity).

    Basis: {E^a F^b K^c : 0 <= a,b,c < N}
    dim = N^3

    Parameters:
        k: integrable level
    """

    def __init__(self, k: int):
        self.k = k
        self.N = k + 2  # order of q^2
        self.q = np.exp(1j * np.pi / (k + 2))
        self.q2 = self.q ** 2  # primitive N-th root of unity
        self.dim = self.N ** 3

    def verify_root_of_unity(self) -> dict:
        """Verify q is the correct root of unity."""
        results = {}
        results['q'] = self.q
        results['q^2'] = self.q2
        results['|q|'] = abs(self.q)
        results['q^(2N)'] = self.q2 ** self.N
        results['|q^(2N) - 1|'] = abs(self.q2 ** self.N - 1)
        # q^2 should be a primitive N-th root of unity
        for m in range(1, self.N):
            results[f'|q^(2*{m}) - 1|'] = abs(self.q2 ** m - 1)
        return results

    def quantum_integer(self, n: int) -> complex:
        """[n]_q = (q^n - q^{-n}) / (q - q^{-1})."""
        if abs(self.q - self.q ** (-1)) < 1e-15:
            return float(n)  # classical limit
        return (self.q ** n - self.q ** (-n)) / (self.q - self.q ** (-1))

    def quantum_factorial(self, n: int) -> complex:
        """[n]_q! = [1]_q [2]_q ... [n]_q."""
        result = 1.0 + 0j
        for i in range(1, n + 1):
            result *= self.quantum_integer(i)
        return result

    def simple_modules(self) -> List[dict]:
        """List simple modules of ubar_q(sl_2).

        At a primitive 2N-th root of unity (q = e^{i*pi/N}),
        the simple modules of the restricted quantum group are
        V_j for j = 0, 1/2, 1, ..., (N-2)/2 = k/2.
        Total: k+1 simple modules.

        Each V_j has dimension 2j+1 (same as classical sl_2).
        """
        modules = []
        for two_j in range(self.k + 1):  # 2j = 0, 1, ..., k
            j = Fraction(two_j, 2)
            dim_j = two_j + 1
            modules.append({
                'spin': j,
                'dim': dim_j,
                'highest_weight': two_j,
            })
        return modules

    def dimension_table(self) -> dict:
        """Compute various dimension data for the restricted QG."""
        mods = self.simple_modules()
        return {
            'k': self.k,
            'N': self.N,
            'q': f'e^(i*pi/{self.N})',
            'dim_ubar': self.dim,
            'num_simples': len(mods),
            'simples': mods,
            'sum_dim_simples_squared': sum(m['dim']**2 for m in mods),
        }

    def multiplication_table_efk(self) -> dict:
        """Verify the defining relations of ubar_q in the largest integrable rep.

        We use the (k+1)-dimensional representation V_{k/2} (the largest
        integrable module). In this module:
          - K eigenvalues are q^k, q^{k-2}, ..., q^{-k}
          - K^N = (-1)^k I (since q^N = e^{i*pi} = -1, so q^{mN} = (-1)^m)
            For EVEN k (integer spin): K^N = I.
            For ODD k (half-integer spin): K^N = -I.
            The algebra relation K^N = 1 holds in the quotient; in the
            spin-j rep, K^N acts as (-1)^{2j} I.
          - E^{k+1} = 0 (nilpotent of order k+1 <= N in this rep)
          - All defining relations of ubar_q are satisfied

        The N-dimensional representation does NOT exist as a simple module
        of ubar_q (its highest weight N-1 exceeds the integrable range k).
        """
        N = self.N
        k = self.k
        q = self.q
        dim_rep = k + 1  # largest integrable module
        checks = {}

        # Basis: |i> for i = 0, ..., k with sl_2 weight = k - 2i
        # K|i> = q^{k-2i} |i>
        K_rep = np.zeros((dim_rep, dim_rep), dtype=complex)
        Kinv_rep = np.zeros((dim_rep, dim_rep), dtype=complex)
        for i in range(dim_rep):
            weight = k - 2 * i
            K_rep[i, i] = q ** weight
            Kinv_rep[i, i] = q ** (-weight)

        # E|i> = sqrt([i]_q [k-i+1]_q) |i-1>  (raises sl_2 weight by 2)
        # F|i> = sqrt([i+1]_q [k-i]_q) |i+1>   (lowers sl_2 weight by 2)
        E_rep = np.zeros((dim_rep, dim_rep), dtype=complex)
        F_rep = np.zeros((dim_rep, dim_rep), dtype=complex)

        for i in range(1, dim_rep):
            coeff_sq = self.quantum_integer(i) * self.quantum_integer(k - i + 1)
            if abs(coeff_sq) > 1e-15:
                E_rep[i - 1, i] = np.sqrt(coeff_sq)

        for i in range(dim_rep - 1):
            coeff_sq = self.quantum_integer(i + 1) * self.quantum_integer(k - i)
            if abs(coeff_sq) > 1e-15:
                F_rep[i + 1, i] = np.sqrt(coeff_sq)

        # Verify relations
        checks['KE = q^2 EK'] = np.allclose(K_rep @ E_rep, q**2 * E_rep @ K_rep)
        checks['KF = q^{-2} FK'] = np.allclose(K_rep @ F_rep, q**(-2) * F_rep @ K_rep)

        lhs = E_rep @ F_rep - F_rep @ E_rep
        rhs = (K_rep - Kinv_rep) / (q - 1/q)
        checks['EF - FE = (K-Kinv)/(q-1/q)'] = np.allclose(lhs, rhs)

        # K^N = (-1)^k I in the spin-k/2 representation
        # (since q^N = e^{i*pi} = -1, and highest weight is k)
        Kpow = np.linalg.matrix_power(K_rep, N)
        expected_sign = (-1) ** k
        checks[f'K^N = ({expected_sign:+d})I (spin {k}/2 rep)'] = \
            np.allclose(Kpow, expected_sign * np.eye(dim_rep))
        # K^{2N} = I always
        K2pow = np.linalg.matrix_power(K_rep, 2 * N)
        checks['K^(2N) = 1'] = np.allclose(K2pow, np.eye(dim_rep))

        # E^N = 0 (nilpotency; in this rep E^{k+1} = 0 since dim = k+1)
        Epow = np.linalg.matrix_power(E_rep, N)
        checks['E^N = 0'] = np.allclose(Epow, np.zeros((dim_rep, dim_rep)))

        # F^N = 0
        Fpow = np.linalg.matrix_power(F_rep, N)
        checks['F^N = 0'] = np.allclose(Fpow, np.zeros((dim_rep, dim_rep)))

        # E^k != 0 (the nilpotency order in this rep is exactly k+1)
        if k >= 1:
            Epow_k = np.linalg.matrix_power(E_rep, k)
            checks[f'E^{k} != 0 (nilpotency order = {k+1})'] = \
                not np.allclose(Epow_k, np.zeros((dim_rep, dim_rep)))

        # Verify quantum Casimir
        # C_q = FE + [K]_q [K^{-1} + 1]_q  (depends on convention)
        # Simpler check: the Casimir eigenvalue on V_j is [j]_q [j+1]_q
        j = Fraction(k, 2)
        casimir_ev = self.quantum_integer(k // 2) * self.quantum_integer(k // 2 + 1)
        checks[f'quantum Casimir eigenvalue [j][j+1] = [{k//2}][{k//2+1}]'] = \
            f'{casimir_ev:.6f}'

        checks['dim PBW basis'] = N ** 3

        return checks


