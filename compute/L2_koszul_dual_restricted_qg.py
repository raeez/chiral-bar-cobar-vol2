class CubicNullVectorAnalysis:
    """Analysis of the cubic null vector f_{(-1)}^3|0> and its
    effect on the bar complex differential.

    At k=2, the null vector is v_null = f_{(-1)}^3|0> at weight 3.
    This is a relation of degree 3 in the generators.

    In the bar complex of V_2, the element [s^{-1}v_null] exists
    in bar degree 1 at weight 3. In the bar complex of L_2,
    v_null = 0, so this element is ABSENT.

    The EFFECT on the Koszul dual: the absence of [s^{-1}v_null]
    from B^1(L_2) means there is a NEW relation in the dual algebra,
    which manifests as a nonzero m_3 operation.

    The m_3 operation is:
      m_3(f, f, f) = something nonzero
    encoding that f^3 = 0 in L_2.

    More precisely, in the Koszul dual of L_2, the cubic relation
    f^3 = 0 becomes the quantum Serre relation F^4 = 0 of the
    restricted quantum group (since N = k+2 = 4).
    """

    def __init__(self, k: int = 2):
        self.k = k

    def null_vector_data(self) -> dict:
        """Data about the null vector at level k."""
        return {
            'level': self.k,
            'null_vector': f'f_({{-1}})^{self.k + 1}|0>',
            'conformal_weight': self.k + 1,
            'sl2_weight': -2 * (self.k + 1),
            'degree_in_generators': self.k + 1,
            'is_quadratic': self.k + 1 == 2,
            'forces_m_n': f'm_{self.k + 1}' if self.k >= 2 else 'none (quadratic)',
        }

    def sl2_weight_space_at_conformal_weight(self, w: int) -> Dict[int, int]:
        """sl_2-weight decomposition of V_k[w] (the conformal weight-w subspace).

        Each state in V_k[w] has a definite sl_2 weight (eigenvalue of h_0).
        The generators contribute: e has sl_2-weight +2, f has -2, h has 0.

        States at conformal weight w are built from partitions of w using
        generators, each colored by {e, f, h}. The sl_2 weight is the
        sum of the individual weights.
        """
        # Build states: a monomial J^{a_1}_{(-n_1)} ... J^{a_m}_{(-n_m)} |0>
        # with sum n_i = w, each n_i >= 1.
        # sl_2 weight = sum of wt(a_i), where wt(e) = 2, wt(f) = -2, wt(h) = 0.

        # For weight w: enumerate all ordered tuples (a_1,n_1),...,(a_m,n_m)
        # with n_1 >= n_2 >= ... >= n_m >= 1 and sum n_i = w.
        # (PBW ordering)

        # Actually, in the universal enveloping algebra of the mode algebra,
        # a PBW basis for weight w is:
        #   product of e_{(-n)}^{a_n} h_{(-n)}^{b_n} f_{(-n)}^{c_n} for n >= 1
        #   subject to sum_n n*(a_n + b_n + c_n) = w

        from itertools import product as cart_prod

        weight_counts: Dict[int, int] = {}

        # For small weights, enumerate directly
        # Weight w: find all tuples (a_n, b_n, c_n) for n=1,...,w
        # such that sum_n n*(a_n + b_n + c_n) = w

        # This is equivalent to 3-colored partitions of w
        # For each partition, the sl_2 weight = sum_n n * (2*a_n - 2*c_n)
        # Wait, no: sl_2 weight is sum of wt contributions:
        # e_{(-n)} contributes sl_2-wt +2, f_{(-n)} contributes -2, h_{(-n)} contributes 0
        # (these are the eigenvalues of [h_0, J^a_{(-n)}] = wt(a) * J^a_{(-n)})

        # So sl_2 weight = 2*(total e-count) - 2*(total f-count)

        # Enumerate: for each way to partition w into colored parts
        # (color = e/f/h, part value = mode number n),
        # the sl_2 weight is 2*(#e-parts) - 2*(#f-parts).

        # Use generating function approach:
        # The 3-colored partition generating function, refined by sl_2 weight:
        # prod_{n>=1} 1/((1-x^2 q^n)(1-x^{-2} q^n)(1-q^n))
        # where x tracks sl_2 weight and q tracks conformal weight.

        # Extract coefficient of q^w as a Laurent polynomial in x.

        max_w = w
        # Build refined character: coeffs[w][sl2_wt] = dim
        # Start with 1 at (0, 0)
        refined = [{} for _ in range(max_w + 1)]
        refined[0][0] = 1

        for n in range(1, max_w + 1):
            new_refined = [{} for _ in range(max_w + 1)]
            for cw in range(max_w + 1):
                for sl2w, count in refined[cw].items():
                    if count == 0:
                        continue
                    # For mode n, add any number of e_{(-n)} (wt +2), f_{(-n)} (wt -2), h_{(-n)} (wt 0)
                    # A state with a_e copies of e_{(-n)}, a_f of f_{(-n)}, a_h of h_{(-n)}
                    # adds n*(a_e + a_f + a_h) to conformal weight
                    # and 2*a_e - 2*a_f to sl_2 weight.

                    # Enumerate (a_e, a_f, a_h) with n*(a_e+a_f+a_h) <= max_w - cw
                    max_total = (max_w - cw) // n
                    for total in range(max_total + 1):
                        for a_e in range(total + 1):
                            for a_f in range(total - a_e + 1):
                                a_h = total - a_e - a_f
                                new_cw = cw + n * total
                                new_sl2w = sl2w + 2 * a_e - 2 * a_f
                                # Multiplicity: multinomial(total; a_e, a_f, a_h)
                                # Wait, these are BOSONIC modes (same mode, different copies)
                                # PBW gives ORDERED monomials, so no multinomial needed.
                                # Actually: e_{(-n)}^{a_e} means a_e applications.
                                # The multiplicity for this choice is 1 (it's a PBW basis element).
                                # But we need to count: for mode n, the contribution
                                # to the character is (1-q^n x^2)^{-1}(1-q^n x^{-2})^{-1}(1-q^n)^{-1}
                                # which at q^{n*total} gives the number of ways to write total = a_e + a_f + a_h
                                # That IS the multinomial coefficient... no.

                                # (1-t)^{-1} = 1 + t + t^2 + ... = sum_{a>=0} t^a
                                # So for each generator, we sum over a >= 0.
                                # The triple product (1-q^n x^2)^{-1}(1-q^n x^{-2})^{-1}(1-q^n)^{-1}
                                # = sum_{a_e,a_f,a_h >= 0} q^{n(a_e+a_f+a_h)} x^{2(a_e-a_f)}
                                # Each choice (a_e, a_f, a_h) gives a distinct PBW monomial.
                                # So the multiplicity for each tuple IS 1.

                                if new_cw <= max_w:
                                    new_refined[new_cw][new_sl2w] = \
                                        new_refined[new_cw].get(new_sl2w, 0) + count
            refined = new_refined

        return refined[w]

    def integrable_truncation_at_weight(self, w: int) -> Dict[int, int]:
        """sl_2-weight decomposition of L_k[w] = V_k[w] / J_k[w].

        The null vector f_{(-1)}^{k+1}|0> has:
        - conformal weight k+1
        - sl_2 weight -2(k+1)

        The ideal J_k is generated as a V_k-module by this vector,
        so J_k[w] = V_k[w-(k+1)] * f^{k+1}|0> + descendants.

        For the sl_2-weight decomposition, J_k[w] at each sl_2-weight
        is obtained from V_k[w-(k+1)] shifted in sl_2-weight by -2(k+1).

        Actually, J_k is a LEFT ideal: it's generated by v_null under
        the action of all J^a_{(-n)} for n >= 1. So a PBW basis element
        of J_k[w] has the form:
          (product of J^a_{(-n)}) * f_{(-1)}^{k+1} |0>
        with total conformal weight = w.
        The modes acting on f^{k+1}|0> add conformal weight w-(k+1)
        and add their own sl_2 weights on top of -2(k+1).
        """
        if w < self.k + 1:
            # Below null vector weight: L_k[w] = V_k[w]
            return self.sl2_weight_space_at_conformal_weight(w)

        vk_decomp = self.sl2_weight_space_at_conformal_weight(w)

        # Compute J_k[w] decomposition:
        # J_k[w] = {modes of weight w-(k+1)} * f^{k+1}|0>
        # The sl_2 weight of (modes * f^{k+1}|0>) =
        #   sl2_wt(modes) + (-2(k+1))
        # where modes have conformal weight w-(k+1)
        null_shift = w - (self.k + 1)
        if null_shift < 0:
            return vk_decomp

        modes_decomp = self.sl2_weight_space_at_conformal_weight(null_shift)
        # Shift by -2(k+1) in sl_2 weight
        ideal_decomp: Dict[int, int] = {}
        for sl2w, count in modes_decomp.items():
            shifted = sl2w - 2 * (self.k + 1)
            ideal_decomp[shifted] = ideal_decomp.get(shifted, 0) + count

        # L_k[w] = V_k[w] / J_k[w]
        result: Dict[int, int] = {}
        all_weights = set(vk_decomp.keys()) | set(ideal_decomp.keys())
        for sl2w in all_weights:
            d = vk_decomp.get(sl2w, 0) - ideal_decomp.get(sl2w, 0)
            if d > 0:
                result[sl2w] = d
            elif d < 0:
                # This shouldn't happen in a correct computation
                result[sl2w] = d  # flag for debugging

        return result


