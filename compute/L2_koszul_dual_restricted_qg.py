class L2BarComplex:
    """Weight-space analysis of the bar complex B^ch(L_2(sl_2)).

    The bar complex is bigraded by (bar degree n, conformal weight w).
    We compute the dimensions of each bigraded piece.

    For V_k(sl_2), the weight-w subspace of bar degree n is:
      B^n(V_k)_[w] = (s^{-1} V_k^+)^{otimes n} restricted to total weight w

    where V_k^+ = bigoplus_{w >= 1} (V_k)_[w] is the augmentation ideal.

    For L_k, the null vector truncation removes descendants of
    f_{(-1)}^{k+1}|0>.

    At k=2:
    - Generators at weight 1: e, f, h (dim 3)
    - Weight 2 of V_2: all products J^a_{(-1)} J^b (dim 3*3 = 9 states
      before Lie symmetry; actual dim of (V_2)_[2] = dim of weight-2
      Verma minus null descendants from OTHER sources)
    - Null vector f_{(-1)}^3|0> lives at weight 3

    The key point: in L_2, the vector f^3 := f_{(-1)}^3|0> vanishes.
    This does NOT remove the weight-3 subspace entirely; it removes
    only the highest-weight vector of the sl_2-module generated by f^3
    and its sl_2-descendants.
    """

    def __init__(self, k: int = 2):
        self.k = k

    def verma_dim(self, weight: int) -> int:
        """Dimension of weight-w subspace of V_k(sl_2).

        The universal affine vertex algebra V_k(sl_2) at level k has
        the same underlying vector space as the vacuum Verma module.
        The character is:
          ch V_k = q^0 / prod_{n>=1} (1-q^n)^3

        where the 3 comes from dim(sl_2) = 3.

        The coefficient of q^w gives dim (V_k)_[w].
        This is the number of partitions of w into parts, with each
        part having 3 colors (one for each generator e, f, h).
        """
        # Use generating function: product_{n>=1} 1/(1-q^n)^3
        # Compute coefficients up to weight `weight`
        max_w = weight + 1
        coeffs = [0] * max_w
        coeffs[0] = 1

        for n in range(1, max_w):
            # Multiply by 1/(1-q^n)^3
            # = sum_{m >= 0} binom(m+2, 2) q^{mn}
            for w in range(max_w - 1, -1, -1):
                # Add contributions from (1-q^n)^{-3}
                # The coefficient of q^w in the product up to factor n
                pass

        # More direct: use partition function with 3 colors
        # p_3(w) = number of ways to write w = sum_i n_i where each n_i >= 1,
        # with 3 choices per part (unordered with multiplicity)
        # This is the coefficient of q^w in prod_{n>=1} (1-q^n)^{-3}

        dims = self._compute_character_coeffs(max_w)
        return dims[weight] if weight < max_w else 0

    def _compute_character_coeffs(self, max_w: int) -> List[int]:
        """Compute coefficients of q^0, ..., q^{max_w-1} in
        prod_{n>=1} (1-q^n)^{-3}."""
        coeffs = [0] * max_w
        coeffs[0] = 1

        for n in range(1, max_w):
            # Multiply current series by (1 - q^n)^{-3}
            # (1-x)^{-3} = sum_{m>=0} C(m+2,2) x^m
            # So multiplying by (1-q^n)^{-3} means:
            # new_coeffs[w] = sum_{m>=0} C(m+2,2) * old_coeffs[w - m*n]
            new_coeffs = [0] * max_w
            for w in range(max_w):
                total = 0
                m = 0
                while w - m * n >= 0:
                    binom_coeff = (m + 1) * (m + 2) // 2  # C(m+2, 2)
                    total += binom_coeff * coeffs[w - m * n]
                    m += 1
                new_coeffs[w] = total
            coeffs = new_coeffs

        return coeffs

    def null_vector_module_dim(self, weight: int) -> int:
        """Dimension of the null module J_k at weight w.

        The null vector f_{(-1)}^{k+1}|0> generates the maximal
        proper ideal J_k. The character of J_k is:

          ch J_k = q^{k+1} * ch V_k (shifted by weight k+1)

        because the ideal is generated by a SINGLE primary vector
        at weight k+1, and the PBW theorem ensures the ideal is
        a free V_k-module generated by the null vector.

        Actually, for sl_2 at integrable level, the ideal is generated
        by f_{(-1)}^{k+1}|0> and its images under the affine Weyl group.
        For the PURPOSE of this computation, the character of L_k is:

          ch L_k = ch V_k - ch J_k

        and ch J_k starts at weight k+1. At weights < k+1, L_k = V_k.
        """
        if weight < self.k + 1:
            return 0
        # The ideal J_k has character q^{k+1} * ch(M) where M is the
        # Verma module generated by f^{k+1}|0>. This Verma has the
        # same character as V_k shifted:
        return self.verma_dim(weight - (self.k + 1))

    def Lk_dim(self, weight: int) -> int:
        """Dimension of weight-w subspace of L_k(sl_2).

        dim L_k[w] = dim V_k[w] - dim J_k[w]

        This is exact for the FIRST null vector. In general, there
        may be more null vectors (from the full Jantzen filtration),
        but for sl_2, the maximal ideal is generated by a single
        primary null vector at each integrable level.
        """
        return self.verma_dim(weight) - self.null_vector_module_dim(weight)

    def bar_degree_1_dims(self, max_weight: int) -> Dict[int, int]:
        """Dimensions of B^1(L_k)[w] = s^{-1}(L_k^+)[w].

        Bar degree 1 is just the desuspended augmentation ideal.
        B^1[w] = L_k[w] for w >= 1.
        """
        return {w: self.Lk_dim(w) for w in range(1, max_weight + 1)}

    def bar_degree_n_Vk_dims(self, n: int, max_weight: int) -> Dict[int, int]:
        """Dimensions of B^n(V_k)[w] = (s^{-1}V_k^+)^{otimes n}[w].

        The weight-w component of the n-fold tensor product of the
        augmentation ideal (each factor has weight >= 1, so total >= n).
        """
        # Start with convolution: dim of n-fold tensor at weight w
        # is sum over partitions w = w_1 + ... + w_n with each w_i >= 1
        # of product of dim V_k[w_i].

        # Build by iterated convolution
        if n == 0:
            return {0: 1}

        # dim of single factor V_k^+ at weight w (w >= 1)
        single = {}
        for w in range(1, max_weight + 1):
            single[w] = self.verma_dim(w)

        result = dict(single)
        for step in range(n - 1):
            new_result = {}
            for w in range(n, max_weight + 1):
                total = 0
                for w1 in range(1, w):
                    w2 = w - w1
                    if w2 in result and w1 in single:
                        # But we need w2 >= step+1 (from previous convolution)
                        total += result.get(w2, 0) * single.get(w1, 0)
                if total > 0:
                    new_result[w] = total
            result = new_result

        return result

    def bar_degree_n_Lk_dims(self, n: int, max_weight: int) -> Dict[int, int]:
        """Dimensions of B^n(L_k)[w].

        Same as V_k but using L_k dimensions for each tensor factor.
        """
        if n == 0:
            return {0: 1}

        single = {}
        for w in range(1, max_weight + 1):
            d = self.Lk_dim(w)
            if d > 0:
                single[w] = d

        result = dict(single)
        for step in range(n - 1):
            new_result = {}
            for w in range(step + 2, max_weight + 1):
                total = 0
                for w1 in range(1, w):
                    w2 = w - w1
                    total += result.get(w2, 0) * single.get(w1, 0)
                if total > 0:
                    new_result[w] = total
            result = new_result

        return result

    def total_bar_dim(self, max_bar_deg: int, max_weight: int) -> int:
        """Total dimension of B^ch(L_k) up to given bar degree and weight."""
        total = 0
        for n in range(1, max_bar_deg + 1):
            dims = self.bar_degree_n_Lk_dims(n, max_weight)
            total += sum(dims.values())
        return total


