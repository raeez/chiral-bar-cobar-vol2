    """Compute {a_λ inner} using right sesquilinearity.

    If inner = Σ c_i · ∂^{n_i} gen_i, then
    {a_λ inner} = Σ c_i · {a_λ ∂^{n_i} gen_i}
                 = Σ c_i · (λ + ∂)^{n_i} · {a_λ gen_i}.
    """
    result: FD = {}
    for (gen, n), coeff in inner.items():
        if gen == 'S':
            continue
        base = m2_gen(a_gen, gen, lam_outer, cv)
        shifted = fd_apply_shift_partial_n(base, lam_outer, n)
        for fld, bc in shifted.items():
            result[fld] = result.get(fld, 0.0) + coeff * bc
    return {k: v for k, v in result.items() if abs(v) > 1e-300}


