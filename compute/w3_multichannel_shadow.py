def compose_left(inner: FD, b_gen: str, lam_outer: float, cv: float) -> FD:
    """Compute {inner_λ b} using left sesquilinearity.

    If inner = Σ c_i · ∂^{n_i} gen_i, then
    {inner_λ b} = Σ c_i · (-λ)^{n_i} · {gen_i_λ b}.
    """
    result: FD = {}
    for (gen, n), coeff in inner.items():
        if gen == 'S':
            # Scalar brackets to zero
            continue
        base = m2_gen(gen, b_gen, lam_outer, cv)
        factor = (-lam_outer)**n
        for fld, bc in base.items():
            key = fld
            result[key] = result.get(key, 0.0) + coeff * factor * bc
    return {k: v for k, v in result.items() if abs(v) > 1e-300}


