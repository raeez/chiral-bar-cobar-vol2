# PART 6: m_3 via Stasheff (W₃)
# =====================================================================

def m3_w3(a: str, b: str, cc: str, l1: float, l2: float, cv: float) -> FD:
    """m₃(a, b, c; l1, l2) from the arity-3 Stasheff identity.

    m₃(a,b,c) = -[{m₂(a,b)_{l1+l2} c} - {a_{l1} m₂(b,c)}]
    where m₂(a,b) = {a_{l1} b} and m₂(b,c) = {b_{l2} c}.
    """
    # Term 1: {m₂(a,b;l1)_{l1+l2} c}
    m2_ab = m2_gen(a, b, l1, cv)
    term1 = compose_left(m2_ab, cc, l1 + l2, cv)

    # Term 2: {a_{l1} m₂(b,c;l2)}
    m2_bc = m2_gen(b, cc, l2, cv)
    term2 = compose_right(a, m2_bc, l1, cv)

    # m₃ = -(term1 - term2)
    return fd_add(term1, term2, signs=[-1.0, 1.0])


