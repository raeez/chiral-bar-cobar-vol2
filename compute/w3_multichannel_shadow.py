# Type alias
FD = Dict[Tuple[str, int], float]


def fd_zero() -> FD:
    return {}

def fd_add(*dicts: FD, signs=None) -> FD:
    """Add field-coeff dicts with optional signs."""
    if signs is None:
        signs = [1.0] * len(dicts)
    result: FD = {}
    for d, s in zip(dicts, signs):
        for f, coeff in d.items():
            result[f] = result.get(f, 0.0) + s * coeff
    return {k: v for k, v in result.items() if abs(v) > 1e-300}


def fd_scale(d: FD, factor: float) -> FD:
    return {f: factor * coeff for f, coeff in d.items() if abs(factor * coeff) > 1e-300}


def fd_apply_partial(d: FD) -> FD:
    """Apply ∂ to a field-coeff dict: ∂(gen, n) = (gen, n+1), ∂(scalar) = 0."""
    result: FD = {}
    for (gen, n), coeff in d.items():
        if gen == 'S':
            continue
        if n + 1 <= MAX_DERIV:
            key = (gen, n + 1)
            result[key] = result.get(key, 0.0) + coeff
    return {k: v for k, v in result.items() if abs(v) > 1e-300}


def fd_apply_shift_partial(d: FD, shift: float) -> FD:
    """Apply (shift + ∂)."""
    return fd_add(fd_scale(d, shift), fd_apply_partial(d))


def fd_apply_shift_partial_n(d: FD, shift: float, n: int) -> FD:
    """Apply (shift + ∂)^n."""
    result = dict(d)
    for _ in range(n):
        result = fd_apply_shift_partial(result, shift)
    return result


