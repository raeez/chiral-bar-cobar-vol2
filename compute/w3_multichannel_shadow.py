                # uses the LAST spectral parameter... or does it use the total sum?
                # Actually for m_2(a, ∂^n b; λ): {a_λ ∂^n b} = (λ+∂)^n {a_λ b}. ✓ Uses λ.
                # For m_3(a, b, ∂^n c; l1, l2): the right sesquilinearity for the
                # last slot uses (l1+l2+∂)^n? Let me check:
                # m_3(a,b,c) = -[left comp] + [right comp]
                # The right comp involves m_2(a, m_2(b,c;l2); l1).
                # If c → ∂^n c: m_2(b, ∂^n c; l2) = (l2+∂)^n m_2(b,c;l2).
                # Then m_2(a, (l2+∂)^n m_2(b,c;l2); l1) = (l1+∂)^n (l2+∂)^n ... no,
                # this is more complex because the right sesquilinearity acts on the
                # ENTIRE inner expression.
                #
                # Actually, the simple rule is:
                # For m_r(a_1,...,a_{slot}=∂^n b,...,a_r) where slot is NOT the last:
                #   factor = (-l_slot)^n (left sesquilinearity)
                # For the LAST slot (slot = r-1):
                #   apply (sum_of_all_lams + ∂)^n to the result of m_r with b instead of ∂^n b
                #   Wait, that's not right either.
                #
                # Let me think about this more carefully.
                # For m_2(a, ∂^n b; λ): the rule is (λ+∂)^n applied to m_2(a,b;λ).
                # For m_3(a, b, ∂^n c; l1, l2): by the recursive structure,
                #   m_3 is built from m_2 compositions. The ∂^n on c propagates
                #   through the recursion. It's NOT simply (l1+l2+∂)^n · m_3(a,b,c).
                #   It's more subtle.
                #
                # HOWEVER: for the purpose of computing m_k(W,...,W), all generators
                # are W (degree 0, no derivatives). The sesquilinearity only matters
                # when the INNER output has derivative content (∂^n gen from intermediate
                # states). And the inner output enters the outer at a specific slot.
                #
                # For the slot < r-1 (not the last): the inner output with ∂^n gen
                # enters via LEFT sesquilinearity: factor (-lam_slot)^n.
                #
                # For the slot = r-1 (the last): the inner output with ∂^n gen
                # enters via RIGHT sesquilinearity: (lam_{r-2}+∂)^n applied to the base.
                # Wait: for m_2(a, inner; λ), the λ used in (λ+∂)^n is the m_2's own λ.
                # For the outer m_r, slot = r-1 means the last input. The m_r recursion
                # builds upon m_2 compositions. The ∂^n on the last input propagates as
                # (last spectral param + ∂)^n applied to the base? Not exactly.
                #
                # ACTUALLY: I think the correct and general rule for ANY slot position is:
                # For slot j in m_r(a_0,...,a_{r-1}; l_0,...,l_{r-2}):
                #   ∂^n a_j enters as:
                #     j = 0: (-l_0)^n (left sesquilinearity, first position)
                #     0 < j < r-1: ... complicated (not simply (-l_j)^n or (l_j+∂)^n)
                #     j = r-1: ... complicated
