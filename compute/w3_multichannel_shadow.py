# The full m_2 dispatcher
def m2_gen(a_gen: str, b_gen: str, lam: float, cv: float) -> FD:
    """Compute m_2(a, b; λ) for generators a, b ∈ {T, W, L}."""
    dispatch = {
        ('T', 'T'): m2_TT,
        ('T', 'W'): m2_TW,
        ('W', 'T'): m2_WT,
        ('W', 'W'): m2_WW,
        ('T', 'L'): m2_TL,
        ('L', 'T'): m2_LT,
    }
    key = (a_gen, b_gen)
    if key in dispatch:
        return dispatch[key](lam, cv)
    # For other combinations (W,L), (L,W), (L,L) — these involve
    # composite fields and are more complex. For the shadow computation
    # we track through field-valued intermediates.
    return fd_zero()


