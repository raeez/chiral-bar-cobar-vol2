class TestW3TranslationOperator:
    """Verify the translation operator d acts correctly."""

    def test_partial_T(self):
        """d(T) = dT."""
        from lib.examples.w3_algebra import apply_partial, T, dT
        assert apply_partial(T) == dT

    def test_partial_dT(self):
        """d(dT) = d2T."""
        from lib.examples.w3_algebra import apply_partial, dT, d2T
        assert apply_partial(dT) == d2T

    def test_partial_W(self):
        """d(W) = dW."""
        from lib.examples.w3_algebra import apply_partial, W, dW
        assert apply_partial(W) == dW

    def test_partial_Lambda(self):
        """d(Lambda) = dLambda."""
        from lib.examples.w3_algebra import apply_partial, Lambda, dLambda
        assert apply_partial(Lambda) == dLambda

    def test_partial_constant(self):
        """d(c) = 0 (translation kills constants)."""
        from lib.examples.w3_algebra import apply_partial, c
        assert apply_partial(c) == 0

    def test_partial_leibniz(self):
        """d(T*W) = dT*W + T*dW (Leibniz rule)."""
        from lib.examples.w3_algebra import apply_partial, T, W, dT, dW
        result = apply_partial(T * W)
        expected = dT * W + T * dW
        assert simplify(result - expected) == 0


