class TestW3Brackets:
    """Verify the four lambda-brackets for W_3."""

    def test_TT_bracket_virasoro(self):
        """Verify {T_lam T} = dT + 2T*lam + (c/12)*lam^3.

        This is the Virasoro subalgebra bracket. The W_3 algebra
        contains Virasoro as a subalgebra generated by T.
        """
        from lib.examples.w3_algebra import (
            w3_lambda_bracket, T, dT, c
        )
        lam = Symbol('lambda')
        result = w3_lambda_bracket('T', 'T', lam)
        expected = dT + 2 * T * lam + Rational(1, 12) * c * lam**3
        assert simplify(result - expected) == 0

    def test_TW_bracket_primary(self):
        """Verify {T_lam W} = dW + 3*W*lam.

        W is primary of conformal weight 3 under T. No anomaly term.
        """
        from lib.examples.w3_algebra import (
            w3_lambda_bracket, W, dW
        )
        lam = Symbol('lambda')
        result = w3_lambda_bracket('T', 'W', lam)
        expected = dW + 3 * W * lam
        assert simplify(result - expected) == 0

    def test_WT_bracket_from_skew(self):
        """Verify {W_lam T} = 2*dW + 3*W*lam.

        From n-products: W_{(1)}T = 3W, W_{(0)}T = 2dW.
        NOTE: this is ASYMMETRIC with {T_lam W} = dW + 3W*lam.
        The asymmetry comes from skew-symmetry with the d operator.
        """
        from lib.examples.w3_algebra import (
            w3_lambda_bracket, W, dW
        )
        lam = Symbol('lambda')
        result = w3_lambda_bracket('W', 'T', lam)
        expected = 2 * dW + 3 * W * lam
        assert simplify(result - expected) == 0

    def test_WW_bracket_pole_orders(self):
        """The {W_lam W} bracket has terms at lambda-degrees 0,1,2,3,5.

        No degree-4 term (no weight-1 state in the vacuum module).
        Maximum degree 5 (from the sixth-order pole W_{(5)}W = c/3).
        """
        from lib.examples.w3_algebra import w3_lambda_bracket
        lam = Symbol('lambda')
        bracket = w3_lambda_bracket('W', 'W', lam)
        p = expand(bracket)
        # Check that degree 4 coefficient vanishes
        # Extract by substituting all fields to 0, keeping lam
        from lib.examples.w3_algebra import _ALL_FIELDS
        p_central = p
        for f in _ALL_FIELDS:
            p_central = p_central.subs(f, 0)
        # p_central should be c/360 * lam^5 (only central term)
        from lib.examples.w3_algebra import c
        assert simplify(p_central - Rational(1, 360) * c * lam**5) == 0

    def test_WW_leading_coefficient(self):
        """Leading term of {W_lam W} is (c/360)*lam^5.

        From W_{(5)}W = c/3 and the factorial: (c/3)*lam^5/5! = c/360*lam^5.
        """
        from lib.examples.w3_algebra import w3_lambda_bracket, c
        lam = Symbol('lambda')
        bracket = w3_lambda_bracket('W', 'W', lam)
        # Extract coefficient of lam^5
        coeff_lam5 = expand(bracket).coeff(lam, 5)
        # Should be c/360 (field-free)
        from lib.examples.w3_algebra import _ALL_FIELDS
        for f in _ALL_FIELDS:
            coeff_lam5 = coeff_lam5.subs(f, 0)
        assert simplify(coeff_lam5 - c / 360) == 0

    def test_WW_cubic_term(self):
        """Coefficient of lam^3 in {W_lam W} is T/3.

        From W_{(3)}W = 2T: (2T)*lam^3/3! = T/3 * lam^3.
        """
        from lib.examples.w3_algebra import w3_lambda_bracket, T
        lam = Symbol('lambda')
        bracket = w3_lambda_bracket('W', 'W', lam)
        coeff_lam3 = expand(bracket).coeff(lam, 3)
        assert simplify(coeff_lam3 - Rational(1, 3) * T) == 0

    def test_WW_quadratic_term(self):
        """Coefficient of lam^2 in {W_lam W} is dT/2.

        From W_{(2)}W = dT: (dT)*lam^2/2! = dT/2 * lam^2.
        """
        from lib.examples.w3_algebra import w3_lambda_bracket, dT
        lam = Symbol('lambda')
        bracket = w3_lambda_bracket('W', 'W', lam)
        coeff_lam2 = expand(bracket).coeff(lam, 2)
        assert simplify(coeff_lam2 - Rational(1, 2) * dT) == 0

    def test_WW_linear_term(self):
        """Coefficient of lam in {W_lam W} is beta_Lambda*Lambda + (3/10)*d2T.

        From W_{(1)}W = (3/10)d^2T + alpha*Lambda.
        """
        from lib.examples.w3_algebra import (
            w3_lambda_bracket, Lambda, d2T, beta_sq
        )
        lam = Symbol('lambda')
        bracket = w3_lambda_bracket('W', 'W', lam)
        coeff_lam1 = expand(bracket).coeff(lam, 1)
        expected = beta_sq * Lambda + Rational(3, 10) * d2T
        assert simplify(expand(coeff_lam1 - expected)) == 0

    def test_WW_constant_term(self):
        """Constant term in {W_lam W} is beta_partial_Lambda*dLambda + (1/15)*d3T.

        From W_{(0)}W = (1/15)d^3T + (alpha/2)*dLambda.
        """
        from lib.examples.w3_algebra import (
            w3_lambda_bracket, dLambda, d3T, beta_partial_Lambda
        )
        lam = Symbol('lambda')
        bracket = w3_lambda_bracket('W', 'W', lam)
        coeff_lam0 = expand(bracket).coeff(lam, 0)
        expected = beta_partial_Lambda * dLambda + Rational(1, 15) * d3T
        assert simplify(expand(coeff_lam0 - expected)) == 0

    def test_beta_squared_formula(self):
        """Verify the standard W_3 nonlinear coefficients.

        The undifferentiated Lambda coefficient is 32/(22+5c);
        the partial-Lambda coefficient is 16/(22+5c).
        """
        from lib.examples.w3_algebra import beta_sq, beta_partial_Lambda, c
        expected_lambda = Rational(32, 1) / (22 + 5 * c)
        expected_partial = Rational(16, 1) / (22 + 5 * c)
        assert simplify(beta_sq - expected_lambda) == 0
        assert simplify(beta_partial_Lambda - expected_partial) == 0

    def test_composite_Lambda_definition(self):
        """Lambda = :TT: - (3/10)*d^2T is quasi-primary of weight 4.

        The factor 3/10 is determined by requiring L_1(Lambda) = 0
        (quasi-primary condition): T_{(2)}(TT) = ... and the (3/10)
        coefficient cancels the non-primary piece.
        """
        # The quasi-primary condition determines the 3/10 coefficient.
        # From T_{(2)}(TT) = 2*(T_{(2)}T)*T + ... = 0 + ...
        # and T_{(2)}(d^2T) = ... (from conformal weight computation)
        # The ratio is 3/10. Verify it appears correctly in the n-products.
        from lib.examples.w3_algebra import w3_nth_product, d2T, Lambda
        result = w3_nth_product('W', 'W', 1)
        # Should be (3/10)*d2T + alpha*Lambda
        from lib.examples.w3_algebra import beta_sq
        expected = Rational(3, 10) * d2T + beta_sq * Lambda
        assert simplify(expand(result - expected)) == 0


